#include <Arduino.h>

#include <ros.h>

/**************************************************************************************
 * PROTOTYPES
 **************************************************************************************/

/**************************************************************************************
 * GLOBAL VARIABLES
 **************************************************************************************/

ros::NodeHandle node_handle;

/**************************************************************************************
 * ARDUINO FRAMEWORK FUNCTIONS
 **************************************************************************************/

void setup() 
{
  node_handle.getHardware()->setBaud(115200);
  node_handle.initNode();
}

void loop() 
{
  node_handle.spinOnce();
}

